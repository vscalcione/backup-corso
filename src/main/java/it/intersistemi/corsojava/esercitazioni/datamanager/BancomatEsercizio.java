package it.intersistemi.corsojava.esercitazioni.datamanager;

public class BancomatEsercizio {
	private  int pin;
	private  int numeroCarta;
	
	

	public BancomatEsercizio( int pin, int numeroCarta) {
		 this.pin= pin;
		 this.numeroCarta= numeroCarta;
	}



	public int getPin() {
		return pin;
	}



	public  int getNumeroCarta() {
		return numeroCarta;
	}
	
	
	
		

}
