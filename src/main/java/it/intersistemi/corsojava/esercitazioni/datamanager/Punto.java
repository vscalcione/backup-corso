package it.intersistemi.corsojava.esercitazioni.datamanager;

/**
 * @author Spellucci
 *
 */
public class Punto {
	private int x;
	private int y;
	
	public Punto() {}
	
	public Punto(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
	return x;
	}
			
	
	
	public int getY() {
	return y;
}
			
	
}
