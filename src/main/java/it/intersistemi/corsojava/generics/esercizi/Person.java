package it.intersistemi.corsojava.generics.esercizi;

public class Person {

	private String nome;
	private String cognome;
	private Sex sesso;


	public Person(String nome, String cognome, Sex sesso) {
		super();
		this.nome = nome;
		this.cognome = cognome;
		this.sesso = sesso;





	}


	public String getNome() {
		return nome;
	}


	public String getCognome() {
		return cognome;
	}





}
