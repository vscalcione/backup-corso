package it.intersistemi.corsojava.amazon.dao.model;


public class Address {
	private Integer id;		
	private String username, nominativo, via, cap, citta, provincia, nazione, numeroTelefono;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getNominativo() {
		return nominativo;
	}
	public void setNominativo(String nominativo) {
		this.nominativo = nominativo;
	}
	public String getVia() {
		return via;
	}
	public void setVia(String via) {
		this.via = via;
	}
	public String getCap() {
		return cap;
	}
	public void setCap(String cap) {
		this.cap = cap;
	}
	public String getCitta() {
		return citta;
	}
	public void setCitta(String citta) {
		this.citta = citta;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getNazione() {
		return nazione;
	}
	public void setNazione(String nazione) {
		this.nazione = nazione;
	}
	public String getNumeroTelefono() {
		return numeroTelefono;
	}
	public void setNumeroTelefono(String numeroTelefono) {
		this.numeroTelefono = numeroTelefono;
	}
	@Override
	public String toString() {
		return "Indirizzo [id=" + id + ", username=" + username + ", nominativo=" + nominativo + ", via=" + via
				+ ", cap=" + cap + ", citta=" + citta + ", provincia=" + provincia + ", nazione=" + nazione
				+ ", numeroTelefono=" + numeroTelefono + "]";
	}
}
